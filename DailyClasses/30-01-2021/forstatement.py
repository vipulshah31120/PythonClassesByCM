list = [2,4,4,6,8,10,12]

# iterating elements of list with for
# in is reserved keyword
for i in list :
    print(i)
    
# we can directly print a list as well
print(list)

# for with slice of list
for i in list[2:] :
    print(i)
    print("hihihi")  #it is in for loop so after every digit 'hihihi' will get printed
    

# for with string
for i in " V i p u l " :
    print(i)

